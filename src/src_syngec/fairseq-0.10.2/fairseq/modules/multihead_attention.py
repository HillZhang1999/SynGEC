"""修改记录
Yue Zhang
2021.12.30
对原有的点积注意力进行修改，原先是序列中的词(seq_len个)对词(seq_len个)，现在是词(seq_len个)对弧(seq_len x seq_len个）做，当然，每个词只对自己的邻边做点积注意力，上述过程还可以优化
"""
def _gat_scaled_dot_product_attention(
    q: Tensor,
    k: Tensor,
    v: Tensor,
    attn_mask,
    dropout_p: float = 0.0,
) -> Tuple[Tensor, Tensor]:
    r"""
    Computes scaled dot product attention on query, key and value tensors, using
    an optional attention mask if passed, and applying dropout if a probability
    greater than 0.0 is specified.
    Returns a tensor pair containing attended values and attention weights.

    Args:
        q, k, v: query, key and value tensors. See Shape section for shape details.
        attn_mask: optional tensor containing mask values to be added to calculated
            attention. May be 2D or 3D; see Shape section for details.
        dropout_p: dropout probability. If greater than 0.0, dropout is applied.

    Shape:
        - q: :math:`(B, Nt, E)` where B is batch size, Nt is the target sequence length,
            and E is embedding dimension.
        - key: :math:`(B, Ns, E)` where B is batch size, Ns is the source sequence length,
            and E is embedding dimension.
        - value: :math:`(B, Ns, E)` where B is batch size, Ns is the source sequence length,
            and E is embedding dimension.
        - attn_mask: either a 3D tensor of shape :math:`(B, Nt, Ns)` or a 2D tensor of
            shape :math:`(Nt, Ns)`.

        - Output: attention values have shape :math:`(B, Nt, E)`; attention weights
            have shape :math:`(B, Nt, Ns)`
    """
    # q: B x T x D
    # k: B x T x T x D
    # v: B x T x T x D

    """Debug记录
    找到为什么注意力权重可能出现全NaN的原因了，
    对于注意力的Pad，我们通过给注意力分数加上-inf来实现，但是这样做，如果一行中所有元素全为-inf，那么softmax的结果必然全为Nan（分母为0）。
    Fairseq之前的Attention Pad虽然如此实现的，但是不会出现Nan的错误，因为它是在batch化时对短句子中的token进行pad补齐，因此不可能存在一个句子中所有的token全部是pad符。
    但是我们这里的Pad是对图的邻接矩阵进行pad，当两个token之间不存在边时，整行元素全为-inf，因此会爆Nan。
    解决方案：
    1）换用mask_fill的方式对attention矩阵进行Pad；
    2）修改softmax的公式，在分母上加一个eps=1e-8
    3) torch.nan_to_num
    """
    B, T, D = q.shape
    # q = q / math.sqrt(D)  # Todo：论文里没加scale，不知道是否要加？ 看了知乎上的回答，好像只需要Attend特定弧时，可以不加scale
    q = q.unsqueeze(-1).contiguous().view(B * T, D, 1)  # q: B*T x D x 1
    k = k.contiguous().view(B * T, T, D)  # k: B*T x T x D
    # attn = torch.bmm(k, q).view(B, T, T)
    attn = F.leaky_relu(torch.bmm(k, q).view(B, T, T), 0.2)  # attn: B x T x T
    attn = softmax(attn, dim=-1)
    if dropout_p > 0.0:
        attn = dropout(attn, p=dropout_p)
    output = torch.mul(attn.unsqueeze(-1), v)  # output: B x T x T x D

    output = output.sum(2)  # output: B x T x D，同一节点所有邻边表示求和
    return output, attn


def _in_projection(
    q: Tensor,
    k: Tensor,
    v: Tensor,
    w_q: Tensor,
    w_k: Tensor,
    w_v: Tensor,
    b_q: Optional[Tensor] = None,
    b_k: Optional[Tensor] = None,
    b_v: Optional[Tensor] = None,
) -> Tuple[Tensor, Tensor, Tensor]:
    r"""
    Performs the in-projection step of the attention operation. This is simply
    a triple of linear projections, with shape constraints on the weights which
    ensure embedding dimension uniformity in the projected outputs.
    Output is a triple containing projection tensors for query, key and value.
    Args:
        q, k, v: query, key and value tensors to be projected.
        w_q, w_k, w_v: weights for q, k and v, respectively.
        b_q, b_k, b_v: optional biases for q, k and v, respectively.
    Shape:
        Inputs:
        - q: :math:`(Qdims..., Eq)` where Eq is the query embedding dimension and Qdims are any
            number of leading dimensions.
        - k: :math:`(Kdims..., Ek)` where Ek is the key embedding dimension and Kdims are any
            number of leading dimensions.
        - v: :math:`(Vdims..., Ev)` where Ev is the value embedding dimension and Vdims are any
            number of leading dimensions.
        - w_q: :math:`(Eq, Eq)`
        - w_k: :math:`(Eq, Ek)`
        - w_v: :math:`(Eq, Ev)`
        - b_q: :math:`(Eq)`
        - b_k: :math:`(Eq)`
        - b_v: :math:`(Eq)`
        Output: in output triple :math:`(q', k', v')`,
         - q': :math:`[Qdims..., Eq]`
         - k': :math:`[Kdims..., Eq]`
         - v': :math:`[Vdims..., Eq]`
    """
    Eq, Ek, Ev = q.size(-1), k.size(-1), v.size(-1)
    assert w_q.shape == (Eq, Eq), f"expecting query weights shape of {(Eq, Eq)}, but got {w_q.shape}"
    assert w_k.shape == (Eq, Ek), f"expecting key weights shape of {(Eq, Ek)}, but got {w_k.shape}"
    assert w_v.shape == (Eq, Ev), f"expecting value weights shape of {(Eq, Ev)}, but got {w_v.shape}"
    assert b_q is None or b_q.shape == (Eq,), f"expecting query bias shape of {(Eq,)}, but got {b_q.shape}"
    assert b_k is None or b_k.shape == (Eq,), f"expecting key bias shape of {(Eq,)}, but got {b_k.shape}"
    assert b_v is None or b_v.shape == (Eq,), f"expecting value bias shape of {(Eq,)}, but got {b_v.shape}"
    return linear(q, w_q, b_q), linear(k, w_k, b_k), linear(v, w_v, b_v)


